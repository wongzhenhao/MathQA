import json
class KnowledgeExtractorPrompt:
    '''
    '''
    def __init__(self):
        pass

    def system_prompt(self) -> str:
        system_prompt = ""
        return system_prompt

    def prompt(self, question: str, solution: str) -> str:
        """
        构造一个知识提取的提示，要求模型在提取知识点的同时，
        标注其类型（概念、应用、计算）。
        
        Args:
            question: 问题文本
            solution: 解答文本
            
        Returns:
            优化后的提取提示
        """
        
        prompt = f"""作为一名资深的数学教育专家，请对以下问题及其解答进行深入分析，旨在为教师备课和学生学习提供精准的指导。

    具体任务要求如下：
    1.  **识别核心主题**：准确识别问题所涉及的主要数学主题。
    2.  **提取关键知识点**：针对每个主题，列出解决该问题所必需的关键知识点。
    3.  **为每个知识点分类**：根据下方的“类型定义”，为每一个关键点标注其所属类型（概念、应用、计算）。

    ---
    **类型定义**:
    * **概念 (Concept)**: 指对数学定义、定理、公理、性质的理解和记忆。例如，“圆的标准方程形式”、“同位角相等”。
    * **计算 (Calculation)**: 指执行具体的数学运算步骤，得出数值结果的过程。例如，“解一元二次方程”、“求分数加法”。
    * **应用 (Application)**: 指将数学概念或计算方法应用于特定情境或解决综合问题的能力。例如，“利用勾股定理解决实际距离问题”、“建立方程模型”。
    ---

    **待分析内容**:

    **问题**：
    {question}

    **解答**：
    {solution}

    ---
    **输出格式要求**:
    请严格按照以下格式组织您的分析结果，不要添加任何额外的说明。

    主题1：[主题名称]
    关键点 (类型)：[关键点描述]
    关键点 (类型)：[关键点描述]
    ...

    主题2：[主题名称]
    关键点 (类型)：[关键点描述]
    ...
    """
        return prompt
    